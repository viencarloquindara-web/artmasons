export const generateSlug = (title: string) => {
  return title
    .toLowerCase()
    .replace(/[^\w\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();
};

export const getArtworkBySlug = (slug: string) => {
  return ARTWORKS.find((a) => generateSlug(a.title) === slug) || null;
};
